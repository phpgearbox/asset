use JShrink\Minifier as JsMin;
use Gears\Asset\Contracts\Minifier;

class Js implements Minifier
{
	private $source;

	public function __construct($source)
	{
		$this->source = $source;
	}

	public function minify()
	{
		return JsMin::minify($this->source);
	}
}